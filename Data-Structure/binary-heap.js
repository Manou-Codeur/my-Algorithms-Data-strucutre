//heaps are similar to binary search tree, the only deference is that
//binary heap can be maxHeap(always the parent node is greater than its child)
//or minHeap(always the parent node is smaller than its child), not like BST where
//the parent node has two child which one is greater than it's parent and the
//other is smaller
//the heap is emplemented using arrays, so there's no nodes with left and right..
//so the idea is we use maths;
//left = elementIndex*2 + 1
//right = elementIndex*2 + 2
//heap are most used when working with priority queue, and also for sorting
//heap insertion and deletion time's complexity is O(log n) but for search is O(n)

/*

    --maxHeap example--

            10
        5        4
     1     3   2    0   

*/

class MaxBinaryHeap {
  constructor() {
    this.values = [];
  }

  insert(element) {
    this.values.push(element);
    this.bubbleUp();
  }

  bubbleUp() {
    let idx = this.values.length - 1;
    const element = this.values[idx];
    while (idx > 0) {
      let parentIdx = Math.floor((idx - 1) / 2);
      let parent = this.values[parentIdx];
      if (element <= parent) break;
      this.values[parentIdx] = element;
      this.values[idx] = parent;
      idx = parentIdx;
    }
  }

  //there is also the delete method
}

